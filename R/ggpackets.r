#' A package for easier compositing and reusing of common ggplots. 
#' 
#' @details
#' `ggpackets` aims to make composite `ggplot2` functions:
#' 
#' 1. more reusable
#' 1. easier to develop multilayer templates
#' 1. functionally interoperable with ggplot2
#' 1. syntactically similar to ggplot2
#' 
#' `ggpackets` also tries to expose functions that allow for use of the package 
#' at varying levels of syntactic intricacy. At its most clean and 
#' straightforward extreme, `ggpackets` exposes functions which appear 
#' syntactically identical to `ggplot`. As more is demanded from the provided 
#' functions, the package aims to accommodate those needs gradually introducing
#' additional parameters. 
